use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Panier>
 *
 * @method Panier|null find($id, $lockMode = null, $lockVersion = null)
 * @method Panier|null findOneBy(array $criteria, array $orderBy = null)
 * @method Panier[]    findAll()
 * @method Panier[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class PanierRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Panier::class);
    }

    public function save(Panier $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Panier $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    // chercher un panier qui est a etat true 
    // et dont l'utilisateur id = id e l'utilisateur actuel

    public function findPanier($userId){
        if($userId){
            $req = "SELECT * FROM panier
            WHERE etat = 0 AND utilisateur_id = $userId";
    
            $conn = $this->getEntityManager()
                ->getConnection();
    
            $stmt = $conn->prepare($req);
            $resultSet = $stmt->executeQuery();
            return $resultSet->fetchAllAssociative();
        }

    }

    public function buildCommande($userId){
        if($userId){
            $req = "SELECT * FROM panier p
            LEFT JOIN contenu_panier c ON p.id = c.panier_id
            LEFT JOIN produit d ON c.produit_id = d.id
            WHERE p.etat = 1 AND utilisateur_id = $userId";
    
            $conn = $this->getEntityManager()
                ->getConnection();
    
            $stmt = $conn->prepare($req);
            $resultSet = $stmt->executeQuery();
            return $resultSet->fetchAllAssociative();
        }

    }


